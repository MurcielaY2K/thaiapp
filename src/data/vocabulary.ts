// ─── Helper functions ─────────────────────────────────────────────────────────

export function getCardById(id: string): VocabCard | undefined {
  return VOCABULARY.find(c => c.id === id);
}

export function getCardsByCategory(category: VocabCard['category']): VocabCard[] {
  return VOCABULARY.filter(c => c.category === category);
}

export function getCardsByRegion(region: VocabCard['region']): VocabCard[] {
  return VOCABULARY.filter(c => c.region === region);
}

export function getCardsByDifficulty(maxDifficulty: number): VocabCard[] {
  return VOCABULARY.filter(c => c.difficultyRating <= maxDifficulty);
}

export function searchCards(query: string): VocabCard[] {
  const q = query.toLowerCase();
  return VOCABULARY.filter(
    c =>
      c.thai.includes(query) ||
      c.romanization.toLowerCase().includes(q) ||
      c.englishMeaning.toLowerCase().includes(q) ||
      c.englishAlternatives?.some(a => a.toLowerCase().includes(q)) ||
      c.tags?.some(t => t.toLowerCase().includes(q)),
  );
}

export function getRelatedCards(card: VocabCard): VocabCard[] {
  if (!card.relatedCardIds) return [];
  return card.relatedCardIds.map(id => getCardById(id)).filter((c): c is VocabCard => c !== undefined);
}

